export function filterCharacters(filters: Filter) {
  let rarityFilter: any = filters.rarity.length
    ? filters.rarity
    : rarities.map((rarity) => rarity.value);
  let elementFilter: any = filters.element.length ? filters.element : [];
  let weaponFilter: any = filters.weapon.length ? filters.weapon : [];
  let nationFilter: any = filters.nation.length ? filters.nation : [];

  let ltVersionFilter = filters.ltVersion ? filters.ltVersion : 999;
  let gtVersionFilter = filters.gtVersion ? filters.gtVersion : 0;

  return characters
    .filter((character) => {
      return (
        rarityFilter.includes(character.rarity) &&
        !elementFilter.includes(character.element) &&
        !weaponFilter.includes(character.weapon) &&
        !nationFilter.includes(character.nation) &&
        character.version < ltVersionFilter &&
        character.version > gtVersionFilter
      );
    })
    .map((character) => {
      return {
        ...character,
        guarantee: false,
      };
    });
}
